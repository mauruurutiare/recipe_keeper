-- =============================================
-- グループ作成時のRLSエラーを修正するマイグレーション
-- schema_v3.sql を実行済みの環境に追加で実行してください
-- =============================================

-- ============================================================
-- 問題の原因：グループ作成直後にSELECTが失敗する
--
-- グループINSERT → .select()で自分のグループを取得しようとする
-- → SELECTポリシーは「group_membersにいる人のみ参照可」
-- → まだメンバー登録前なので自分のグループが見えない → RLSエラー
--
-- 解決策：オーナーは常に自分のグループを参照できるようにする
-- ============================================================

drop policy if exists "グループメンバーはグループを参照できる" on public.groups;

create policy "グループメンバーはグループを参照できる"
  on public.groups for select
  using (
    owner_id = auth.uid()                        -- オーナーは常に参照可
    or id in (select public.get_my_group_ids())  -- メンバーも参照可
  );
